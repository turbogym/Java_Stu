package CommAndCharacter;

public class ChangeChar {
    public static void main(String[] args) {
        System.out.println("北京\t天津");
        System.out.println("北京\n天津");
        System.out.println("北京\\天津");
        System.out.println("北京\r天津");

        System.out.println("==================");

        System.out.println("书名\t作者\t价格\t销量\n《三国》\t罗贯中\t45\t12000");
//        System.out.println("《三国》\t罗贯中\t45\t12000");
    }
}
