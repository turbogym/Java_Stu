package com.byonecup.OOP_IntermediateSection.super_;

public class Super01 {
    /**
     * 基本介绍
     * super代表父类的引用，用于访问父类的属性、方法、构造器
     *
     * 基本语法
     * 1. 访问父类的属性，但不能访问父类的private属性
     *      super.属性名;
     * 2. 访问父类的方法，不能访问父类的private方法
     *      super.方法名(参数列表);
     * 3. 访问父类的构造器
     *      super(参数列表); // 只能放在构造器的第一句，只能出现一句！
     */
}
